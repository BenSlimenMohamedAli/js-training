// equality: ==, ===, inequity :!=, !==, relational operators : <, <=, >, >=

// equality
console.log(5 == "5"); // Outputs true ( type coercion ) => it checks the value
console.log(5 === "5"); // Outputs false ( strict equality ) => it checks value and type

// Inequality

console.log(5 != "5"); // Outputs false ( type coercion ) => it checks the value
console.log(5 !== "5"); // Outputs true ( strict equality ) => it checks value and type

// Relational Operators
console.log(5 < 10); // true
console.log(5 <= 10); // true
console.log(5 > 10); // false
console.log(5 >= 10); // false

