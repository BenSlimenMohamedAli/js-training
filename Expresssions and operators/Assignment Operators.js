/**
 * =, += , -=, *=, /= %=
 */

// =
let x = 10; // = is an assignment operator
let message = "Hello"; // = is an assignment operator

// +=
x += 2; // equivalent to :  x = x + 2;
console.log(x);

// -=

x -= 2; // equivalent to :  x = x - 2;
console.log(x);

// *=

x *= 2; // equivalent to :  x = x * 2;
console.log(x);

// /=

x /= 2; // equivalent to :  x = x / 2;
console.log(x);

// %=

x %= 2; // equivalent to :  x = x % 2;
console.log(x);
